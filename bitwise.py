a = 6
b = 4
print("using bitwise operator", a & b)  # Bitwise AND
print("using bitwise operator", a | b)  # Bitwise OR    
print("using bitwise operator", a ^ b)  # Bitwise XOR
print("using bitwise operator", ~a)  # Bitwise NOT  
print("using bitwise operator", a << 2)  # Bitwise left shift
print("using bitwise operator", a >> 2)  # Bitwise right shift